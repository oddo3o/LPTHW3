# 'Some' Math Symbols in Python 3.6
# + plus: Addition
# - minus: Subtraction
# / slash: Division
# * asterisk: Multiplication
# % percent: Modulus
# < greater-than: Greater-Than
# > less-than: Less-Than
# <= less-than-equal:Less-than-equal
# >= greater-than-equal: Greater-than-equal

# This creates a string
print("I will now count my chickens:")

# This line has a string, but continues with a math equation
print("Hens", 25.0 + 30 / 6)
print("Roosters", 100.0 - 25 * 3 % 4)
print("Now I will count the eggs:")

# print() runs all math symbols from order of operations
print(3.0 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6)
print("Is it true that 3 + 2 < 5 - 7?")

# Boolean Operation 'True or False'
print(3.0 + 2 < 5 - 7)
print("What is 3 + 2?", 3.0 + 2)
print("WHat is 5 - 7?", 5.0 - 7)
print("Oh that's why it's false.")

# Decimal places add floating points to equation
print("I need to calculate this.")
print("There are 4 seats at a table.")
print("Only 1 person on each seat.")
print("How many people can sit at the table?")
print("The table can seat", 4.0 + 4 - 1 - 3 + 1 - 3 + 2, "people")
